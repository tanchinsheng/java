package q201.operators.decision;

public class app {

    public static void main(String[] args) {
        System.out.println(1 + 5 < 3 + 7);
        System.out.println(1 + (5 < 3) + 7);
        System.out.println(1 + ' ' + 7);
        System.out.println(1 + "" + 7);
        System.out.println((2 + 2) >= 2 + 3);
    }

}

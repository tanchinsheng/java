package q266.loop;

public class NewMain {

    public static void main(String[] args) {

        int i = 0, j = 0;
        for (i = 0; i < 3; ++i) {
        }
        System.out.println(i);
        for (j = 3; j > 0; --j) {
        }
        System.out.println(j);

        for (int k = 0; k < 3; ++k) {
            System.out.println(k);
        }
        System.out.println();
        for (int l = 3; l > 0; --l) {
            System.out.println(l);
        }
        System.out.println();
    }

}
